# A variable stores information
name = "Rania Rashid"        # string variable (text)
age = 20                      # integer variable (whole number)
university = "Ghazi University DG Khan"  # string variable
semester = 2                  # integer variable

# print() displays output on the screen
print("Hello World!")         # prints simple text

# f-string lets you use variables inside print
print(f"My name is {name}")
print(f"I am {age} years old")
print(f"I study at {university}")
print(f"I am in semester {semester}")

# You can print multiple things in one line
print(f"Hi! I am {name}, an AI student at {university} 🚀")

